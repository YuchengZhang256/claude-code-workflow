#!/usr/bin/env python3
"""
proof-audit OPEN_PROBLEMS.md generator (Phase δ.2).

Reads `state.json` and emits a human-readable markdown file listing every
finding that triage put in Tier O (genuine open problem requiring new
technique or human judgment, not just a fixable patch).

For each Tier-O finding the generated section contains:

  ## <claim_label> — <short title from gap_type>
  - finding_id, current_severity, first surfaced in round N
  - Why it's open: rationale from the most recent tier_history entry
  - What's been tried: prior fix_attempts (last 3, summarized)
  - What would close it: notes field if present, otherwise auto-generated
    "this would be closed if ..." stub for the user to expand
  - Anchor / package pointer for the patcher

The script is idempotent — re-running it overwrites the output cleanly.

CLI:
  open_problems.py emit  --state state.json [--out OPEN_PROBLEMS.md]
                          [--include-deferred-human]
                          [--package-dir proof_audit/claim_packages]
"""

import argparse
import datetime as _dt
import json
import sys
from pathlib import Path
from typing import Any, Dict, List, Optional


DEFAULT_STATE = "proof_audit/state.json"
DEFAULT_OUT = "OPEN_PROBLEMS.md"
DEFAULT_PACKAGES = "proof_audit/claim_packages"


def _ts() -> str:
    return _dt.datetime.now(_dt.timezone.utc).isoformat(timespec="seconds")


def _load_state(path: str) -> Dict[str, Any]:
    p = Path(path)
    if not p.exists():
        print(f"ERROR: {p} not found", file=sys.stderr)
        sys.exit(2)
    return json.loads(p.read_text())


def _short_title(gap_type: str) -> str:
    """Convert a gap_type like 'undirected-block-dependence' into 'Undirected
    block dependence' for use as a section heading."""
    if not gap_type:
        return "(unspecified)"
    return gap_type.replace("-", " ").replace("_", " ").strip().capitalize()


def _format_attempt(att: Dict[str, Any]) -> str:
    """Format one fix_attempt as a 1-line bullet."""
    parts: List[str] = []
    parts.append(f"r{att.get('round', '?')}")
    if att.get("commit"):
        parts.append(f"`{att['commit']}`")
    if att.get("anchor"):
        parts.append(f"`{att['anchor']}`")
    if att.get("approach"):
        parts.append(att["approach"])
    outcome = att.get("outcome", "?")
    return f"  - [{outcome}] " + " · ".join(parts)


def _resolve_package_path(packages_dir: Path, claim_id: str,
                          claim_label: str) -> Optional[str]:
    """Try to find a package path for this claim. Returns relative string or None."""
    if not packages_dir or not packages_dir.exists():
        return None
    candidate = packages_dir / f"{claim_id}.json"
    if candidate.exists():
        return str(candidate)
    return None


def emit_markdown(
    state: Dict[str, Any],
    include_deferred: bool,
    packages_dir: Optional[Path],
) -> str:
    """Render the OPEN_PROBLEMS.md content."""
    out: List[str] = []
    out.append(f"# Open Problems")
    out.append("")
    out.append(
        f"_Auto-generated by proof-audit at {_ts()} from "
        f"`{state.get('manuscript', '?')}` (round {state.get('current_round', '?')})._"
    )
    out.append("")
    out.append(
        "Each entry below was classified as **Tier O** by `triage.py` — it requires "
        "new technique or human judgment to resolve, NOT a routine patch. "
        "The auto-fix loop will not retry these; surface them to the human reviewer "
        "or send them to the next paper."
    )
    out.append("")

    # Collect entries
    entries: List[Dict[str, Any]] = list(state.get("open_problems", []))
    if include_deferred:
        for f in state.get("findings", []):
            if f.get("lifecycle_status") == "deferred_human":
                entries.append(f)

    if not entries:
        out.append("_No open problems recorded yet._")
        return "\n".join(out)

    # Sort: highest severity first, then earliest first_round
    entries.sort(
        key=lambda e: (
            -(e.get("current_severity") or 0),
            (e.get("first_round") or 0),
        )
    )

    # Quick TOC table
    out.append("## Index")
    out.append("")
    out.append("| sev | id | claim | gap_type | first_rd | rounds_seen |")
    out.append("|----:|----|-------|----------|---------:|------------:|")
    for e in entries:
        out.append(
            f"| {e.get('current_severity', '?')} | "
            f"{e.get('finding_id', '?')} | "
            f"`{e.get('claim_label', '?')}` | "
            f"`{e.get('gap_type', '?')}` | "
            f"{e.get('first_round', '?')} | "
            f"{len(e.get('rounds_flagged', []))} |"
        )
    out.append("")

    # Per-entry detail
    for e in entries:
        cid = e.get("claim_id") or e.get("finding_id", "?")
        label = e.get("claim_label", "(no label)")
        gap = e.get("gap_type", "unspecified")
        sev = e.get("current_severity", "?")
        first_rd = e.get("first_round", "?")
        rounds_seen = len(e.get("rounds_flagged", []))
        last_tier = (e.get("tier_history") or [{}])[-1]
        rationale = last_tier.get("rationale") or "(no rationale recorded)"
        attempts = e.get("fix_attempts") or []
        excerpt = (e.get("excerpt") or "").strip()

        out.append(f"## {label} — {_short_title(gap)}")
        out.append("")
        out.append(f"- **finding_id**: `{e.get('finding_id', '?')}`  ")
        out.append(f"- **claim_id**: `{cid}`  ")
        out.append(f"- **current severity**: {sev}  ")
        out.append(f"- **first surfaced**: round {first_rd}  "
                   f"(seen in {rounds_seen} round(s) total)  ")
        if e.get("sources"):
            out.append(f"- **sources**: {', '.join(e['sources'])}  ")
        pkg_path = _resolve_package_path(
            packages_dir or Path(DEFAULT_PACKAGES), cid, label,
        )
        if pkg_path:
            out.append(f"- **package**: `{pkg_path}`  ")
        out.append("")

        out.append("### Why this is an open problem")
        out.append("")
        out.append(f"> {rationale}")
        out.append("")

        if excerpt:
            out.append("### Excerpt from the most recent flagging")
            out.append("")
            out.append("```")
            out.append(excerpt[:600])
            out.append("```")
            out.append("")

        if attempts:
            out.append("### What's been tried")
            out.append("")
            for att in attempts[-5:]:
                out.append(_format_attempt(att))
            out.append("")
        else:
            out.append("### What's been tried")
            out.append("")
            out.append("- _no fix attempts recorded_")
            out.append("")

        if e.get("notes"):
            out.append("### Notes")
            out.append("")
            out.append(e["notes"])
            out.append("")

        out.append("### What would close this")
        out.append("")
        # Auto-generate a stub the user can expand. The right closure depends on
        # which gap-type bucket the finding fell into, so we give a few hints.
        gap_lower = (gap or "").lower()
        if "scope" in gap_lower or "model class" in gap_lower:
            out.append(
                "- A constructive proof for the broader model class, OR an "
                "explicit scope-narrowing in the statement (e.g. \"sharp for "
                "$\\beta < 1/2$ only\")."
            )
        elif "citation" in gap_lower or "external" in gap_lower:
            out.append(
                "- A primary-source argument that does not rely on the cited "
                "external theorem (re-derive the needed conclusion in-paper), "
                "OR a precise citation chain showing the cited result implies "
                "the stronger form used here."
            )
        elif "open" in gap_lower or "future" in gap_lower:
            out.append(
                "- New mathematical technique. Out of scope for this draft. "
                "Park here for follow-up paper."
            )
        else:
            out.append(
                "- _(human judgment required — describe the closure criterion "
                "you would accept)_"
            )
        out.append("")

    out.append("---")
    out.append("")
    out.append(
        f"_End of OPEN_PROBLEMS.md ({len(entries)} entries)._ "
        "To regenerate: `python3 scripts/open_problems.py emit --state proof_audit/state.json`."
    )
    return "\n".join(out)


def _cmd_emit(args):
    state = _load_state(args.state)
    pdir = Path(args.packages_dir) if args.packages_dir else None
    md = emit_markdown(state, args.include_deferred_human, pdir)
    Path(args.out).write_text(md)
    n_open = len(state.get("open_problems", []))
    n_def = sum(
        1 for f in state.get("findings", [])
        if f.get("lifecycle_status") == "deferred_human"
    )
    extra = f" (+ {n_def} deferred_human)" if args.include_deferred_human else ""
    print(f"Wrote {args.out} with {n_open} open problem(s){extra}")


def main():
    ap = argparse.ArgumentParser(description="proof-audit OPEN_PROBLEMS.md generator")
    sub = ap.add_subparsers(dest="cmd", required=True)

    p = sub.add_parser("emit", help="emit OPEN_PROBLEMS.md from state.json")
    p.add_argument("--state", default=DEFAULT_STATE)
    p.add_argument("--out", default=DEFAULT_OUT)
    p.add_argument("--include-deferred-human", action="store_true",
                   help="also include findings with lifecycle_status=deferred_human")
    p.add_argument("--packages-dir", default=DEFAULT_PACKAGES,
                   help="claim_packages/ for package_path injection")
    p.set_defaults(func=_cmd_emit)

    args = ap.parse_args()
    args.func(args)


if __name__ == "__main__":
    main()
